//search users 
router.get("/search", async (req, res) => {
  const users = await User.find();
  const { q } = req.query;

  const keys = ["username", "email"];

  const search = (data) => {
    return data.filter((item) =>
      keys.some((key) => item[key].toLowerCase().includes(q))
    );
  };

  res.status(200).json(search(users).splice(0, 20));
});

//random users
router.get("/random/:username", async (req, res) => {
  const users = await User.find();

  const shuffled = users.sort(() => 0.5 - Math.random());
  let randomUsers = [];
  shuffled.map(u => u.username !== req.params.username && randomUsers.push(u));
  res.status(200).json(randomUsers.slice(0, 5));
})

